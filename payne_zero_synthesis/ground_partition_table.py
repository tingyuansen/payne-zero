_GROUND_PARTITION_HCK = (
    REFERENCE_PLANCK_ERG_SECOND * LIGHT_SPEED_CM_PER_S / REFERENCE_BOLTZMANN_ERG_PER_K
)


def ground_partition_value(label: int, temperature: float) -> float:
    """Return one ground-state partition correction."""
    if temperature <= 0.0:
        return 1.0
    try:
        base_weight, excited_terms = GROUND_PARTITION_TERMS[int(label)]
    except KeyError:
        return 1.0
    partition = base_weight
    for statistical_weight, energy_cm in excited_terms:
        partition += statistical_weight * math.exp(
            -_GROUND_PARTITION_HCK / temperature * energy_cm
        )
    return partition


def ground_partition_values(label: int, temperature: np.ndarray) -> np.ndarray:
    """Vector of ground-state partition corrections for a temperature column."""
    temperature_array = np.asarray(temperature, np.float64)
    partition = np.ones_like(temperature_array)
    positive_temperature = temperature_array > 0.0
    if not np.any(positive_temperature):
        return partition
    partition[positive_temperature] = [
        ground_partition_value(int(label), float(temperature_value))
        for temperature_value in temperature_array[positive_temperature]
    ]
    return partition
